package loilt.mobile;

public class UpdateMobileError {

    private String priceError;
    private String descriptionError;
    private String quantityError;
    private String yearError;

    public UpdateMobileError() {
    }

    /**
     * @return the priceError
     */
    public String getPriceError() {
        return priceError;
    }

    /**
     * @param priceError the priceError to set
     */
    public void setPriceError(String priceError) {
        this.priceError = priceError;
    }

    /**
     * @return the descriptionError
     */
    public String getDescriptionError() {
        return descriptionError;
    }

    /**
     * @param descriptionError the descriptionError to set
     */
    public void setDescriptionError(String descriptionError) {
        this.descriptionError = descriptionError;
    }

    /**
     * @return the quantityError
     */
    public String getQuantityError() {
        return quantityError;
    }

    /**
     * @param quantityError the quantityError to set
     */
    public void setQuantityError(String quantityError) {
        this.quantityError = quantityError;
    }

    /**
     * @return the yearError
     */
    public String getYearError() {
        return yearError;
    }

    /**
     * @param yearError the yearError to set
     */
    public void setYearError(String yearError) {
        this.yearError = yearError;
    }

}
